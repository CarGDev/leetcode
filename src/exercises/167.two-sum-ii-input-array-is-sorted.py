class Solution:
    def twoSum(self, numbers: List[int], target: int) -> List[int]:
        i = 0
        j = len(numbers) - 1
        while i < j:
            need = target - numbers[i]
            if need == numbers[j]:
                break
            if need < numbers[j]:
                j -= 1
            elif need > numbers[i]:
                i += 1
            
        return [i + 1, j + 1]


        
